# Arithmetic Operators :-

print("15 + 3 is ", 15 + 3) # Addition
print("15 - 3 is ", 15 - 3) # Substraction
print("15 * 3 is ", 15 * 3) # Multiplication
print("15 / 3 is ", 15 / 3) # Division
print("15 // 3 is ", 15 // 3) # Floor Division
print("15 % 3 is ", 15 % 3) # Modulo( Gives Only Remainder )
print("15 **3 is ", 15 ** 3) # Exponential

# Assignment Operators :-

x = 9 # x Assigned a Value Which is 9
print(x)
x += 7
print(x)

# Comparison Operators :-

i = 45
print(i == 45)
print(i != 45)

# Logical Operators :-

a = True
b = False
print(a and b)
print(a or b)

# Identity Operators :-

print(a is not b)
print(a is not a)

# Membership Operators :-

list = [4,5,45,66,64,18,9,36,69,27,10,75,99]
print(32 in list)
print(32 not in list)
print(27 in list)

# Bitwise Operators :- # Work With Bit(Binary) Numberes

# 0 - 00 , 1 - 01 , 2 - 10 , 3 - 11

print(0 & 1)
print(0 | 1)

